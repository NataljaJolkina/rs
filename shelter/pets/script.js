// & KATRINE START
const modalKatrine = document.querySelector('.modal__katrine');
const buttonKatrine = document.querySelector('.katrine__button');
const closeKatrine = document.querySelector('.katrine__close');

buttonKatrine.onclick = function () {
    modalKatrine.style.display = "block";
}

closeKatrine.onclick = function () {
    modalKatrine.style.display = "none";
}

window.onclick = function () {
    if(event.target == modalKatrine){
        modalKatrine.style.display = "none";
    }
}
// & KATRINE END

